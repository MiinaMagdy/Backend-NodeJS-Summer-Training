let mathFunctions = [];
const add = (a, b) => a + b;
const subtract = (a, b) => a - b;
const muliply = (a, b) => a * b;
const divide = (a, b) => a / b;
const modulus = (a, b) => a % b;
mathFunctions.push(add);
mathFunctions.push(subtract);
mathFunctions.push(muliply);
mathFunctions.push(divide);
mathFunctions.push(modulus);
module.exports = mathFunctions
